def caesar(text, shift, direction):
    if direction == "encode":
        text_list = list(text)
        cipher_list = []

        for i in text_list:
            if i in alphabet:
                original = alphabet.index(i)
                addition = original + shift
                result = alphabet[addition]
                cipher_list.append(result)
            else:
                cipher_list.append(i)
        
        cipher = ''.join(cipher_list)
        print(f"The encoded text is {cipher}.")
    elif direction == "decode":
        text_list = list(text)
        user_list = []

        for i in text_list:
            if i in alphabet:
                original = alphabet.index(i)
                subtraction = original - shift
                result = alphabet[subtraction]
                user_list.append(result)
            else:
                user_list.append(i)
        
        plain_text = ''.join(user_list)
        print(f"The encoded text is {plain_text}.")

